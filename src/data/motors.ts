export const getPowersByBrand = (brand: MotorBrand): number[] => {
  return [...new Set(motorModels
    .filter(m => m.brand === brand)
    .map(m => m.power)
  )].sort((a, b) => a - b);
};

export const getModelsByBrandAndPower = (brand: MotorBrand, power: number): MotorModel[] => {
  return motorModels.filter(m => m.brand === brand && m.power === power);
};
